def solution(k, d):
    ans = 0
    for x in range(0, d+1, k):
        for y in range(0, d+1, k):
            print(f"x: {x}, y: {y}, dist: {(x**2+y**2)**0.5}")
            if (x**2+y**2)**0.5 <= d:
                ans += 1
    return ans


print(solution(2, 4)) # 6
print(solution(1, 5)) # 26

